/**
 * Literature is an abstract class representing general literary works.
 * Subclasses should provide implementation for the getTitle method.
 *
 * @author Dalraj Bains
 * @author Farzad Farzin
 * @author Anil Bronson
 * @author Arsh Mann
 *
 * @version 1.0
 */
public abstract class Literature{

    /**
     * Gets the title of the literary work.
     *
     * @return the title of the literary work.
     */
    public abstract String getTitle();
}
